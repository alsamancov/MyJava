package Programmr.com.IntegerDataType1;

/**
 * Created by Саманцов on 05.06.2015.
 *
 * The below example demonstrates how to use 2 variables, a and b.
 It assigns both with a calculated arithmetic value and displays
 the output.
 The example prints the value of the two variables as output.
 */
public class IntegerDataType1 {
    public static void main(String[] args){
        int a = (20 - 3) * (3 - 9) / 3;
        int b = 20 - 3 * 3 - 9 / 3;
        System.out.println(a);
        System.out.println(b);
    }
}
